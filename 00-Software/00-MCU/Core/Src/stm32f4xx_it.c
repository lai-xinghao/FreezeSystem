/**************************串口中断回调函数（IDLE+DMA自定义）*********************************/
void IDLE_DMA_Callback(void)
{
	if(recv1_end_flag == 1)		//串口1，调试用
	{
		switch(rx1_buffer[0])
		{
			case 'A':

				HAL_GPIO_TogglePin(Debug_Led_GPIO_Port, Debug_Led_Pin);

				break;

			case 'B':

				HAL_GPIO_TogglePin(Debug_Led_GPIO_Port, Debug_Led_Pin);

				break;

			case 'C':

				break;

			default:
				//HAL_GPIO_TogglePin(Debug_Led_GPIO_Port, Debug_Led_Pin);
				printf("请输入正确的指令！\r\n");
		}

		memset(rx1_buffer, 0, rx1_len);
		rx1_len = 0;//清除计数
		recv1_end_flag = 0;//清除接收结束标志位
		HAL_UART_Receive_DMA(&huart1,rx1_buffer,256);//重新打开DMA接收
	}
}
